function HomePage(props) {
  return (
    <>
      <Head>
        <title>Meetups Home Page</title>
        <meta
          name='description'
          content='One of the best online meetup website'
        />
      </Head>
      <MeetupList meetups={props.meetups} />
    </>
  );
}

